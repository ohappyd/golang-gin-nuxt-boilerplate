
<template>
<div class="vsm-item"
    :class="[{'first-item' : firstItem}, {'open-item' : show}, {'active-item' : active}, {'parent-active-item' : childActive}]"
    @mouseenter="mouseEnter($event)">
    <template v-if="isRouterLink">
        <router-link class="vsm-link"
            :to="item.href"
            :disabled="item.disabled"
            :event="item.disabled ? '' : 'click'"
            @click.native="clickEvent">
            <i v-if="item.icon"
                class="vsm-icon"
                :class="getIcon(item)" />
            <template v-if="!(isCollapsed || isCompacted)">
                <span v-if="item.badge"
                    :style="[rtl ? (item.child ? {'margin-left' : '0px'} : '') : (item.child ? {'margin-right' : '0px'} : '')]"
                    class="vsm-badge"
                    :class="[item.badge.class ? item.badge.class : 'default-badge']">{{ item.badge.text }}</span>
                <span class="vsm-title">{{ item.title }}</span>
                <i v-if="item.child"
                    class="vsm-arrow"
                    :class="{'open-arrow' : show}" />
            </template>
        </router-link>
    </template>
    <template v-else>
        <a class="vsm-link"
            :href="item.href ? item.href : '#'"
            :disabled="item.disabled"
            @click="clickEvent">
            <i v-if="item.icon"
                class="vsm-icon"
                :class="getIcon(item)" />
            <template v-if="!isCollapsed">
                <span v-if="item.badge"
                    :style="[rtl ? (item.child ? {'margin-left' : '0px'} : '') : (item.child ? {'margin-right' : '0px'} : '')]"
                    class="vsm-badge"
                    :class="[item.badge.class ? item.badge.class : 'default-badge']">{{ item.badge.text }}</span>
                <span class="vsm-title">{{ item.title }}</span>
                <i v-if="item.child && !item.badge"
                    class="vsm-arrow"
                    :class="{'open-arrow' : show}" />
            </template>
        </a>
    </template>
    <template v-if="item.child">
        <template v-if="!(isCollapsed || isCompacted)">
            <transition name="expand"
                @enter="expandEnter"
                @afterEnter="expandAfterEnter"
                @beforeLeave="expandBeforeLeave">
                <div v-if="show"
                    class="vsm-dropdown">
                    <div class="vsm-list small-scroll">
                        <sub-item v-for="(subItem, index) in item.child"
                            :key="index"
                            :item="subItem" />
                    </div>
                </div>
            </transition>
        </template>
    </template>
</div>
</template>
<script>
import SubItem from './SubItem.vue'
import { itemMixin, animationMixin } from '@/util/menu/mixin.js'

/*
Item menu from sidebar menu
*/
export default {
  components: {
    SubItem
  },
  mixins: [itemMixin, animationMixin],
  props: {
    // Item to render
    item: {
      type: Object,
      required: true
    },
    // Show only first item
    firstItem: {
      type: Boolean,
      default: false
    },
    // Is collapsed mode
    isCollapsed: {
      type: Boolean
    },
    // Is compacted mode
    isCompacted: {
      type: Boolean
    },
    iconIndex: {
      type: Number,
      default: 0
    }

  },
  methods: {
    getIcon (item) {
      return Array.isArray(item.icon) ? item.icon[this.iconIndex] : item.icon
    },
    // On mouse enter show submenu
    mouseEnter (event) {
      if ((this.isCollapsed || this.isCompacted) && this.firstItem) {
        this.$parent.$emit('mouseEnterItem', {
          item: this.item,
          pos:
              event.currentTarget.getBoundingClientRect().top -
              this.$parent.$el.getBoundingClientRect().top,
          height: this.$el.offsetHeight
        })
      }
    }
  }
}

</script>



